public class JavaMath {
    public static void main(String[] args) {
        System.out.println(Math.max(8,6));
        System.out.println(Math.min(8,6));
        System.out.println(Math.sqrt(64));
        System.out.println(Math.abs(-4.7));//return positive value
        System.out.println(Math.random()); // return randon number between 0.0 and 0.9
        

    }
}
